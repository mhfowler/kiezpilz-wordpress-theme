Kiezpilz is a citizen-science collective unfolding the social and ecological benifits of communal fungi cultivation. We are making use of unused spaces and urban waste streams. Our goal is reproducable, community-based fungi cultivation.
<br/><br/>
We see fungi cultivation as a portal towards more sustainable ways of living, community empowerment, and reciprocal connection with non-human life.
<br/><br/>
Dreams
<br/><br/>
We dream of communities that enrich the natural world around them and enrich eachother in the process. Moving away from extraction and exploitation towards solidarity and autonomy. People empowered to define their own lives in cooperation with eachother and the non-human coconspiritors around them.
<br/><br/>
Fungi close the loop of natural cycles. They turn the fallen leaves into into rich living soil that supports the growth of a new tree. Fungi show us the connection of old and new, of death and rebirth.
<br/><br/>
We hope to foster community resilence by working with fungi - both as direct practice and as a source for reflection and metaphor.
<br/><br/>
Methods
<br/><br/>
Using low-tech and DIY methods, we cultivate mushrooms at a community scale. Our first space is a small oyster mushroom farm in the basement of Top Lab in Schillerkiez Neukolln. We grow the mushrooms using locally sourced waste materials as substrate, using sawdust from a local woodshop and coffee grounds from local coffee shops.
<br/><br/>
We encourage you to reproduce our experiment in your home or your neighborhood and are happy to share material and knowledge.
